// tools/build-css.mjs — Compile the panel's Tailwind input into a static CSS
// string and emit src/ui/styles.generated.mjs (committed, so build:bookmarklet
// works without the Tailwind toolchain — like build:catalog vs build:bookmarklet).
//
//   bun run build:css   → regenerate after changing panel classes or tailwind.css
//
// Scans src/ui/** for utility classes (see tailwind.config.cjs).

import { spawnSync } from "node:child_process";
import { readFileSync, writeFileSync, mkdtempSync, rmSync } from "node:fs";
import { resolve, dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { tmpdir } from "node:os";

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const input = resolve(ROOT, "src/ui/tailwind.css");
const out = resolve(ROOT, "src/ui/styles.generated.mjs");

const tmp = mkdtempSync(join(tmpdir(), "m3e-css-"));
const cssFile = join(tmp, "out.css");

// Pin the CLASSIC panel to Tailwind v3 explicitly. The modern theme adds
// @tailwindcss/cli (v4) as a devDep, and both packages claim the same
// `tailwindcss` bin — so node_modules/.bin/tailwindcss resolves to whichever
// was installed last (v4). Invoking the v3 package's CLI directly (via the
// current runtime) keeps classic on v3 and its output byte-identical, no matter
// what owns the shared bin. Modern compiles via tools/build-css.modern.mjs.
const v3cli = resolve(ROOT, "node_modules/tailwindcss/lib/cli.js");
const res = spawnSync(process.execPath, [v3cli, "-i", input, "-o", cssFile, "--minify"], {
  cwd: ROOT,
  encoding: "utf8",
  stdio: ["ignore", "pipe", "pipe"],
});
if (res.status !== 0) {
  console.error(res.stderr || res.stdout || "tailwindcss 失败");
  rmSync(tmp, { recursive: true, force: true });
  process.exit(1);
}

const css = readFileSync(cssFile, "utf8");
rmSync(tmp, { recursive: true, force: true });

const banner =
  "// src/ui/styles.generated.mjs — GENERATED by tools/build-css.mjs. Do not edit.\n" +
  "// Precompiled Tailwind CSS, inlined into the panel's Shadow DOM <style>.\n";
writeFileSync(out, `${banner}export const PANEL_CSS = ${JSON.stringify(css)};\n`);
console.error(`[build:css] ${(css.length / 1024).toFixed(1)}KB → src/ui/styles.generated.mjs`);
